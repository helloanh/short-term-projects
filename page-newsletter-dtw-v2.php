								<h2>CCC On The Move</h2>
								<p class="text-justify">The Fair Immigration Reform Movement (FIRM) displayed a powerful show of force, solidarity, and commitment to families in March. During a three-day summit for its 44 groups, FIRM held workshops to strengthen our movement and strategize on the best ways to protect immigrants from the Trump Administration’s heinous policies. Progressive leaders from ally organizations and several Members of Congress joined our discussions; all committed to fighting for comprehensive immigration reform. </p>
								 
								<p class="text-justify">The summit ended with a rousing action in front of the U.S. Customs and Border Protection headquarters in downtown D.C. The rally featured impassioned speeches by impacted families, FIRM leaders, and Sen. Bernie Sanders, whose office tweeted out a video of the rally that was retweeted more than 4,500 times. Several media outlets published stories on the event including Huffington Post and <a href="https://www.facebook.com/plugins/video.php?href=https%3A%2F%2Fwww.facebook.com%2Fmundohispanico%2Fvideos%2F1753235468026588%2F&show_text=0&width=560">Mundo Hispanico </a>that featured an interview with Immigration Campaign Manager Sulma Arias. Kica Matos, Director of Immigrant Rights and Racial Justice, said while many in our communities are afraid “I saw amazing courage in your willingness to stand up and be heard.”</p>
								 
								<p class="text-justify">Leading up to the summit FIRM leaders and progressive allies from across the country gathered to build a strategy for massive turnout and participation on May 1st, International Workers Day. The discussion centered on building a human wall around President Trump that derails his attacks on immigrants and throws sand in the gears of his deportation machine. FIRM has launched, “RiseUp! May 1st,” a call to action for Americans of all walks of life to stand between immigrant families and the attacks of the Trump administration. More than 75 progressive leaders participated, including Indivisible, FWD.us, Black Alliance for Justice Immigration, Human Rights Campaign, Mi Familia Vota, People’s Action, Center for Popular Democracy, SEIU, Planned Parenthood Federation of America, and many, many others. </p>

								<p class="text-justify">Our progressive allies are all in on May 1st and ready to join our national call to action. Together, we will mobilize immigrant allies to stop splintering families, condoning the hate, and leading the reckless attacks leveled by bullies like Trump, DHS Secretary John Kelly, and Attorney General Jeff Sessions. </p>

