							<div class="image"><a href="https://www.washingtonpost.com/news/post-nation/wp/2017/03/29/a-holocaust-survivors-pointed-message-about-ice-enforcement-history-is-not-on-your-side/?utm_term=.f7ebffe326cb"><img src="https://communitychange.org/wp-content/uploads/2017/04/manuel-pastor.png" alt="bernard marks" /></a></div><div class="content">
									<h3>CCC’s Board Member Manuel Pastor in The New York Times</h3>
										<p>CCC’s Putting Families First Advisory Council and board member Manuel Pastor was quoted in the New York Times for a story entitled <a href="https://www.nytimes.com/2017/02/01/upshot/strife-over-immigrants-can-california-foretell-nations-future.html?emc=eta1">Immigrant Shock: Can California Predict the Nation’s Future</a>. In the piece, Pastor, a University of Southern California sociologist, recounts California’s history with immigration and explains how the demographic change in California between 1980 and 2000 mirrors the change (and projected change) in the United States since 2000 and up to 2050, when white are expected to be less than half of the nation’s population. The question, he says, is whether the rest of the country can adjust faster to demographic change — or with less conflict — than California did. “Why go through all of our pain? That was no fun, and it dashed a lot of people’s lives. We underinvested in education. We over-imprisoned, so we got a lot of people locked out of the labor market. We broke apart a lot of families because of anti-immigrant sentiments. We did a lot of stupid things to ourselves.”</p>
