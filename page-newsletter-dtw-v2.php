							<h3>From Our Writing Fellow</h3>
									<p>
									CCC writing fellow Thomas Kennedy, who came to the U.S. with his family as a child and grew up as an undocumented immigrant, wrote a piece on the devastating impact of a lack of health care on families. In a piece published on the Huffington Post called <a href="http://www.huffingtonpost.com/entry/affordable-health-care-repeal-or-how-to-steal-from_us_58c8342ee4b0816ed87b5e38">“Affordable Health Care Repeal: Or How To Steal From The Poor And Give To The Rich,” </a> Thomas focuses on the GOP’s American Health Care Act (AHCA) , its  highway robbery on the American people, and its result: 24 million Americans without healthcare. As we know, raising the voices of impacted people such as Thomas had a huge effect on how we succeeded in sinking the Republican Party’s efforts to repeal the Affordable Care Act.
									</p>
