							<header class="major">
								<p class="text-left">
								Last May 1st, we reflected on the “Immigrant Spring” of 2006 when boycotts and massive demonstrations erupted in response to the infamous “Sensenbrenner Bill” – a set of draconian immigration enforcement measures that passed the House and were later defeated in the Senate. The protests culminated in the marches of May 1st, called A Day Without Immigrants, in which millions of immigrants and their allies flooded the streets in cities across the country. That day, I was in the New Haven Green, taking part in a march and rally with thousands of other New Haven residents. <br><br>

								Our voices prevented the passage of a violently anti-immigrant bill and raised the public discourse on the issue of unauthorized immigration and the need for humane, comprehensive immigration reform. We inspired the subsequent decade of voter registration, movement building for immigrant rights organizations, and a generation of young activists who remain at the forefront of our movement.  <br><br>

								Flash forward to 2017: In fewer than 100 days, we have faced Executive Orders and Cabinet confirmations that call into question the fundamental values of this country as one that promotes equality, inclusion and justice for all.  <br><br>

								Scenes are playing out -- at schools, private homes, grocery stores, and workplaces across the country: children watching their parents taken into custody by Immigration and Customs Enforcement (ICE) Officers. ICE Officers showing up at courtrooms to detain victims of domestic violence and surveilling hypothermia shelters on winter mornings to intercept people whose only crime is a lack of paperwork. We face looming threats of mass deportations as well as the termination of Deferred Action for Childhood Arrivals (DACA). <br><br>

								Our show of resistance to these threats must be equal to our outrage. This assault on our values must be a call to action for millions of allies to join the fight ahead. <br><br>

								On May 1st, CCC and our partners will participate in a national day of action: RiseUp! May 1. In cities, towns, and communities across the country hundreds of thousands of people will rise up in resistance. We will demonstrate the power, resilience, and strength of immigrant communities in America alongside our 44 partners in <a href="https://fairimmigration.org">Fair Immigration Reform Movement (FIRM)</a>. Just as FIRM groups helped lead the rise-up at airports in January, we will help lead these May 1st marches during this critical time in American political history. <br><br>

								In 100 cities, in 24 states,<a href="https://reformimmigrationforamerica.org/rise-up/"> Rise Up!</a> partners will hold marches, rallies, student walkouts and more.  We will RiseUp! in solidarity with all those targeted by Trump’s hate – our DACAmented colleagues, our Muslim community, our LGBTQ sisters and brothers. We will RiseUp! for black lives and in defense of our planet. We will again make history on this day and, together with our brothers and sisters in the progressive movement, we will stand united in our defiance. Our solidarity will be the foundation for the years of resistance ahead. <br><br>

